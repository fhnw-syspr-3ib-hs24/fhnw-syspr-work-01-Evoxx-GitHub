#include <stdio.h>

int main() {
    // Ausgabe der Größe der Basistypen
    printf("sizeof(char)   = %d Byte\n", (int) sizeof(char));
    printf("sizeof(int)    = %d Byte\n", (int) sizeof(int));
    printf("sizeof(long)   = %d Byte\n", (int) sizeof(long));
    printf("sizeof(float)  = %d Byte\n", (int) sizeof(float));
    printf("sizeof(double) = %d Byte\n", (int) sizeof(double));

    return 0;
}